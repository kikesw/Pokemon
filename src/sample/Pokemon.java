package sample;

public class Pokemon {
    int Vida;
    int Vidacurada;
    int Nivel;
    String Nombre;
    String desc;

    Pokemon(int Vida, int Nivel, String Nombre, int Vidacurada, String desc){
        this.Vida = Vida;
        this.Vidacurada = Vidacurada;
        this.Nivel = Nivel;
        this.Nombre = Nombre;
        this.desc=desc;
    }

}
